package day04_variables;

public class Olympics {
    public static void main(String[] args) {

        /*
        China 2022
        France 2024
        Italy 2026
         */

        int year;

        System.out.println("The Olympics will be in these locations in teh coming years:");

        year = 2022;

        System.out.println("Look forward to the year " + year + " for the Olympics to be in China.");

        year = 2024;

        System.out.println("Look forward to the year " + year + " for the Olympics to be in France.");

        year = 2026;

        System.out.println("Look forward to the year " + year + " for the Olympics to be in Italy.");

        // This is how to reassign a variable and have it output as different values in lines of code.
        // This is also how to concatenate different types of output.
    }

}
