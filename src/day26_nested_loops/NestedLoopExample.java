package day26_nested_loops;

public class NestedLoopExample {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("hello world");
            for (int j = 0; j < 3; j++) {
                System.out.println("hello universe");
            }


        }

    }
}
