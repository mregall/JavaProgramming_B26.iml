package day29_array;

public class ForEachLoop {
    public static void main(String[] args) {
        /*

        For Each Loop:

        for(datatype variableName : array/collection){
        }

        ex: int [] arr = {10, 20, 30};

        for(int num : arr){
            System.out.println(num);
            }

        iteration 1:
            num 10, prints 10
        iteration 2:
            num 20, prints 20
        iteration 3:
            num 30, prints 30

       The array[i] is the same as num (loop variable)

       ==========================================
       Use for each:
            when you need to run through each element of the array
            when you need read only values, nothing needs to be changed.

       Don't use for each:
            when you need index numbers
            when you might need to change something in the data

         */
    }
}
