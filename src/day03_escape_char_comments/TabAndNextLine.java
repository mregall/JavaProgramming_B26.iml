package day03_escape_char_comments;

public class TabAndNextLine {
    public static void main(String[] args) {
        System.out.println("My name starts with 'M'");

        System.out.println("This was tabbed once");
        System.out.println("\tThis was tabbed once");

        // You could use the tab key and have the same effect as \t but its not good style

        //Next line - you can use a blank sout to make a styly blank line.

        //To use a single sout for multiple lines use \n "next line"

        System.out.println("\n1. Go to store\n2. Get milk\n3. Wash car");



    }
}
